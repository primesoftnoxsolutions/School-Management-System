function buildAdmissionNo(index) {
  return `REG-${YEAR}-${String(index + 1).padStart(6, "0")}`;
}

export const seedStudents = async () => {
  let created = 0;
  let skipped = 0;

  for (let i = 0; i < SAMPLE_STUDENTS.length; i += 1) {
    const row = SAMPLE_STUDENTS[i];
    const admissionNo = buildAdmissionNo(i);

    const exists = await Student.findOne({
      isDeleted: false,
      $or: [
        { admissionNo },
        { firstName: row.firstName, lastName: row.lastName, className: row.className, section: row.section },
      ],
    }).lean();

    if (exists) {
      skipped += 1;
      continue;
    }

    await Student.create({
      ...row,
      admissionNo,
      guardianName: row.fatherName,
      dateOfBirth: new Date(row.dateOfBirth),
      admissionDate: new Date(row.admissionDate),
      status: "ACTIVE",
      maritalStatus: "SINGLE",
      createdBy: "system",
      updatedBy: "system",
    });

    created += 1;
  }

  console.log(`Students seed complete: ${created} created, ${skipped} skipped.`);
  return { created, skipped };
};
